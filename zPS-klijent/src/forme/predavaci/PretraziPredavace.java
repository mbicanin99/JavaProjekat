    private void btnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniActionPerformed
        int index = tblPredavaci.getSelectedRow();
        if (index == -1) {
            JOptionPane.showMessageDialog(rootPane, "Izaberite predavača koga želite da izmenite!", "Greška", JOptionPane.INFORMATION_MESSAGE);
        } else {
            Predavac izabrani = (Predavac) mtp.getLista().get(index);
            DodajIzmeniPredavaca df = new DodajIzmeniPredavaca(this, izabrani, ulogovaniMenadzer);
            df.setVisible(true);
            this.setVisible(false);
        }
    }//GEN-LAST:event_btnIzmeniActionPerformed

