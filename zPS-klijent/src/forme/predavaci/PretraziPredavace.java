    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed
        int index = tblPredavaci.getSelectedRow();
        if (index == -1) {
            JOptionPane.showMessageDialog(rootPane, "Izaberite predavača koga želite da obrišete!", "Greška", JOptionPane.INFORMATION_MESSAGE);
        } else {
            try {
                Predavac izabraniPredavac = (Predavac) mtp.getLista().get(index);
                int odluka = JOptionPane.showConfirmDialog(this, "Da li ste sigurni da želite da obrišete izabranog predavača " + izabraniPredavac.toString() + "?", "", JOptionPane.WARNING_MESSAGE);
                if (odluka == 0) {
                    Predavac u = (Predavac) Kontroler.vratiInstancu().obrisiPredavaca(izabraniPredavac);
                    JOptionPane.showMessageDialog(rootPane, "Obrisali ste podatke o predavaču " + u.toString(), "Uspeh", JOptionPane.INFORMATION_MESSAGE);
                    srediTabelu();
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(rootPane, "Došlo je do greške prilikom brisanja predavača!", "Greška", JOptionPane.ERROR_MESSAGE);

            }
        }
    }//GEN-LAST:event_btnObrisiActionPerformed

    private void btnPretraziActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPretraziActionPerformed

        try {
            String ime = txtImePredavaca.getText().trim();
            if (ime.isEmpty()) {
                List<AbstractObjekat> listaPredavaca = Kontroler.vratiInstancu().ucitajPredavace();
                mtp = new ModelTabelePredavaci(listaPredavaca);
                tblPredavaci.setModel(mtp);
            } else {

                List<AbstractObjekat> listaPredavaca = Kontroler.vratiInstancu().pretraziPredavace(ime);

                if (listaPredavaca.isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Sistem ne može da nađe predavače po zadatoj vrednosti!");
                } else {
                    mtp = new ModelTabelePredavaci(listaPredavaca);
                    tblPredavaci.setModel(mtp);
                }
            }
        } catch (ValidacijaException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Greška", JOptionPane.ERROR_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Greška", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnPretraziActionPerformed

