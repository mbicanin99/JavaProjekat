    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed
        int index = tblUcesnici.getSelectedRow();
        if (index == -1) {
            JOptionPane.showMessageDialog(rootPane, "Izaberite učesnika koga želite da obrišete!", "Greška", JOptionPane.INFORMATION_MESSAGE);
        } else {
            try {
                Ucesnik izabraniUcesnik = (Ucesnik) mtu.getLista().get(index);
                int odluka = JOptionPane.showConfirmDialog(this, "Da li ste sigurni da želite da obrišete izabranog ucesnika " + izabraniUcesnik.toString() + "?", "", JOptionPane.WARNING_MESSAGE);
                if (odluka == 0) {
                    Ucesnik u = (Ucesnik) Kontroler.vratiInstancu().obrisiUcesnika(izabraniUcesnik);
                    JOptionPane.showMessageDialog(rootPane, "Obrisali ste podatke o ucesniku " + u.toString(), "Uspeh", JOptionPane.INFORMATION_MESSAGE);
                    srediTabelu();
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(rootPane, "Došlo je do greške prilikom brisanja učesnika!", "Greška", JOptionPane.ERROR_MESSAGE);

            }
        }
    }//GEN-LAST:event_btnObrisiActionPerformed

    private void btnPretraziActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPretraziActionPerformed

        try {
            String ime = txtImeUcesnika.getText().trim();
            if (ime.isEmpty()) {
                List<AbstractObjekat> listaUcesnika = Kontroler.vratiInstancu().ucitajUcesnike();
                mtu = new ModelTabeleUcesnici(listaUcesnika);
                tblUcesnici.setModel(mtu);
            } else {
                List<AbstractObjekat> listaUcesnika = Kontroler.vratiInstancu().pretraziUcesnike(ime);
                if (listaUcesnika.isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Sistem ne može da nađe učesnike po zadatoj vrednosti!");
                } else {
                    mtu = new ModelTabeleUcesnici(listaUcesnika);
                    tblUcesnici.setModel(mtu);
                }

            }
        } catch (ValidacijaException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Greška", JOptionPane.ERROR_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Greška", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnPretraziActionPerformed

